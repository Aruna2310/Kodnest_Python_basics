### int to str
a = 10
print(a)#10
print(type(a))#<class 'int'>
b = str(a)
print(b)#10
print(type(b))#<class 'str'>

### float to str
a = 10.2345
print(a)#10.2345
print(type(a))#<class 'float'>)
b = str(a)
print(b)#10.2345
print(type(b))#<class 'str'>

